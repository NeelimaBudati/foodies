package com.respac;

public class Drinks {
	private String cococola;
	private String thumbsup;
	private String sprite;
	private String mocktails;
	public String getCococola() {
		return cococola;
	}
	public void setCococola(String cococola) {
		this.cococola = cococola;
	}
	public String getThumbsup() {
		return thumbsup;
	}
	public void setThumbsup(String thumbsup) {
		this.thumbsup = thumbsup;
	}
	public String getSprite() {
		return sprite;
	}
	public void setSprite(String sprite) {
		this.sprite = sprite;
	}
	public String getMocktails() {
		return mocktails;
	}
	public void setMocktails(String mocktails) {
		this.mocktails = mocktails;
	}

}