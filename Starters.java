public class Starters {
    
	private String frenchfries;
	private String pizza;
	private String burger;
	private String smiley;
	private String manchurian;
	public String getFrenchfries() {
		return frenchfries;
	}
	public void setFrenchfries(String frenchfries) {
		this.frenchfries = frenchfries;
	}
	public String getPizza() {
		return pizza;
	}
	public void setPizza(String pizza) {
		this.pizza = pizza;
	}
	public String getBurger() {
		return burger;
	}
	public void setBurger(String burger) {
		this.burger = burger;
	}
	public String getSmiley() {
		return smiley;
	}
	public void setSmiley(String smiley) {
		this.smiley = smiley;
	}
	public String getManchurian() {
		return manchurian;
	}
	public void setManchurian(String manchurian) {
		this.manchurian = manchurian;
	}
	
}
